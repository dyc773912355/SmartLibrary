public class Counts {
    private String sname;
    private int iname;
    private int counts;

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public int getIname() {
        return iname;
    }

    public void setIname(int iname) {
        this.iname = iname;
    }

    public int getCounts() {
        return counts;
    }

    public void setCounts(int counts) {
        this.counts = counts;
    }
}
